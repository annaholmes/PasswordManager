package Unsorted;

public class PasswordLabel {
	private String label;
	private String password;
	 
	  public PasswordLabel(String label, String password) {
	    this.label = label;
	    this.password = password;
	  }
	  
	  public String getLabel() {
		  return label;
	  }
	  
	  public String getPassword() {
		  return password;
	  }

}
